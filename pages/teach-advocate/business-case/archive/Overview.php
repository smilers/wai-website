   <p>This page is the first of five web pages covering  the <a href="soc.html" shape="rect"><strong>social</strong></a>, <a href="tech.html" shape="rect"><strong>technical</strong></a>, <a href="fin.html" shape="rect"><strong>financial</strong></a>, and <a href="pol.html" shape="rect"><strong>legal and policy</strong></a> factors in the business case for Web accessibility. Each page presents different aspects of Web accessibility along with guidance on developing a customized business case.</p>
   <h2><a name="intro" id="intro" shape="rect"></a>Introduction</h2>
   <p>The Web is increasingly an essential resource for many aspects of life: education, employment, government, commerce, health care, recreation, social interaction, and more. The Web is used not only for receiving information, but also for providing information and interacting with
      society. Therefore, it is <strong>essential that the Web be accessible in order to provide equal access and equal opportunity to people with disabilities</strong>.  Indeed, the <a href="http://www.un.org/disabilities/default.asp?navid=12&amp;pid=150" shape="rect"><acronym title="United Nations">UN</acronym> Convention on the Rights of Persons with Disabilities</a> (2006) recognizes Web accessibility as a basic human right. </p>
   <p>While  accessibility focuses on people with disabilities, it also benefits older  users, mobile device users, and other individuals, as well as organizations. Older users with age-related accessibility needs are an increasingly  important customer base for most organizations, as the percentage of older users  is  increasing significantly.  Organizations with accessible websites   benefit from search engine optimization (SEO), reduced legal  risk, demonstration of corporate social responsibility (CSR), and increased customer loyalty.</p>
   <p>Organizations can realize substantial return on investment (ROI) that offset any  costs of implementing Web accessibility. In order to be willing to make the initial investment, many organizations need to
      understand the social, technical, and financial benefits of Web accessibility, and the expected returns. The justification to commit resources to a project is often called a
      "business case". Business cases usually document an analysis of a project's
      value in meeting the organization's objectives, the cost-benefit analysis,
      and the expected outcomes.</p>
   <h2><a name="factors" id="factors" shape="rect"></a>Factors in a Business Case for Web Accessibility</h2>
   <p class="listintro">The different aspects of the business case for Web accessibility are presented in detail in the following pages:</p>
   <ul>
      <li class="listspaced"><a href="soc.html" shape="rect"><strong>Social Factors</strong></a> addresses
         the role of Web accessibility in providing equal opportunity for people with disabilities; the overlap with digital divide issues; and benefits to people <em>without</em> disabilities, including people using mobile devices, older people, people with low literacy and people not fluent in the language, people with low bandwidth connections to the Internet, people using older technologies, and new and infrequent web users.</li>
      <li class="listspaced"><a href="tech.html" shape="rect"><strong>Technical Factors</strong></a> addresses
         interoperability, quality, reducing site development and maintenance
         time, reducing server load, enabling content on different configurations,
         and being prepared for advanced web technologies.</li>
      <li class="listspaced"><a href="fin.html" shape="rect"><strong>Financial Factors</strong></a> addresses the financial benefits of increased website use, for example,  from engine optimization (SEO); direct cost savings; considerations for  initial costs and on-going costs; and ways to decrease costs, including  <a href="http://www.w3.org/WAI/mobile/overlap" shape="rect">developing for accessibility and mobile together</a>.</li>
      <li class="listspaced"><a href="pol.html" shape="rect"><strong>Legal and Policy Factors</strong></a> addresses
         requirements for Web accessibility from governments and other
         organizations in the form of laws, policies, regulations, standards,
         guidelines, directives, communications, orders, or other types of
         documents.</li>
   </ul>
   <p>To help develop a customized business case, each of these pages starts with questions to help identify how the factors  apply to a
      specific organization.</p>
