   <h2><a name="custom" id="custom" shape="rect"></a>Developing a Customized Business
      Case</h2>
   <p>An effective business case focuses on the organization's objectives and
      motivations. Certain aspects of the value and outcomes of Web accessibility are more important to one organization than another, based on its particular
      situation. For example, one organization's motivation might be to demonstrate
      social responsibility by being inclusive of people with disabilities, while
      another organization's primary motivation might be technical quality and
      meeting international standards.</p>
   <p>Just as organizations' objectives and motivations vary, so do their business cases for Web accessibility. A customized
      business case for a specific organization will have different content and
      style, and incorporate different aspects with different emphasis, focused on that particular organization.</p>
   <h3><a name="samples" id="samples" shape="rect"></a>Examples of How Factors Differ Across Environments</h3>
   <p class="listintro">Examples of different  motivations that organizations  have for adopting accessibility and how those can be integrated into a customized business case are provided below:</p>
   <ul class="listwithp">
      <li>A <strong>corporation</strong> might emphasize:
         <ul>
               <li>financial gains and cost savings from increased web use due to
                  increased potential market share, search engine optimization (SEO), and
                  increased usability</li>
            <li>reducing risk of legal action, high legal expenses, and
               negative image</li>
            <li>public relations benefits of demonstrating corporate social
               responsibility (CSR)</li>
            <li>benefits of an inclusive workplace that supports employees with disabilities</li>
            <li> increased productivity from supporting and retaining  older employees and their experience</li>
         </ul>
      </li>
      <li>A <strong>government ministry or agency</strong> might emphasize:
         <ul>
               <li>laws and policies that require public services are available to all, such as anti-discrimination
                  legislation or information and communications technology (ICT) policy</li>
            <li>demonstration of social responsibility and provision of information and services that are accessible to all citizens</li>
            <li>savings from improved server performance and decreased
               site maintenance</li>
            <li>benefits from enabling people with disabilities and older users to interact with them online instead of more costly ways</li>
         </ul>
      </li>
      <li>An <strong>educational institution</strong> might emphasize:
         <ul>
               <li>number of students, faculty, or staff with disabilities in
                  educational settings as a social responsibility consideration</li>
            <li>benefits of Web accessibility to students with different
               learning styles, older computer equipment, or low bandwidth Internet
               connections</li>
            <li>benefits to older employees who may be experiencing age-related impairments, an increasing percentage of employees as the workforce ages</li>
            <li>legal or policy requirements</li>
         </ul>
      </li>
      <li>A <strong>non-governmental (non-profit) organization</strong> (NGO) might emphasize:
         <ul>
               <li>social responsibility factors, such as the organization's
                  commitment to human rights, including the right to information</li>
            <li>organization's interest in ensuring that its materials are
               available to all of its potential audiences</li>
            <li>supporting older people with age-related impairments who may be donors</li>
         </ul>
      </li>
      <li>A <strong>web design business</strong> might emphasize:
         <ul>
               <li>competitive advantage and financial benefits of being able to
                  meet increasing requests for accessible web design and development</li>
            <li>technical benefits and long term financial benefits to clients
               of providing sites according to web standards</li>
            <li>public relations benefits of being able to support clients'
               interests in demonstrating social responsibility</li>
         </ul>
      </li>
      <li>A <strong>small to medium-sized enterprise</strong> (SME) relying on e-Commerce might emphasize:
         <ul>
               <li> positive impact on search engine optimization (SEO) from accessibility improvements</li>
            <li> importance of an increasing market among people with disabilities and older people who may significantly benefit from accessible online shopping</li>
            <li> increased general usability and trustworthiness  of online shopping interfaces from improved accessibility</li>
            <li> reduced risk of   legal action and negative publicity from not complying with anti-discrimination legislation</li>
         </ul>
      </li>
   </ul>
   <p class="listintro">While your business case may emphasize a few aspects, it is also important that you include other aspects. For
      example:</p>
   <ul class="listwithp">
      <li>An organization with a clear legal requirement for Web accessibility
         might still need a solid cost/benefit analysis to get management to
         commit sufficient resources for an effective Web accessibility
         program.</li>
      <li>Some commercial organizations might be more responsive to a social
         responsibility argument than to a standard cost/benefit analysis when
         committing to Web accessibility.</li>
   </ul>
   <h2><a name="related" id="related" shape="rect"></a>Additional Resources</h2>
   <p>The following resources provide additional details related to the business case for accessibility:</p>
   <dl>
      <dt><a href="http://www.w3.org/WAI/intro/wai-age-literature.php" shape="rect"><strong>Web Accessibility for Older Users</strong>: A Literature Review</a> </dt>
      <dd>Illustrates the increasing importance of older users online and the overlap between the Web accessibility requirements of older people with age-related impairments and the provisions of the <a href="http://www.w3.org/WAI/intro/wcag20" shape="rect">Web Content Accessibility Guidelines 2.0</a>. Includes  statistics about changing demographics and the prevalence of impairment with age.</dd>
      <dt><a href="http://www.w3.org/WAI/mobile/overlap" shape="rect"><strong>Web Content Accessibility and Mobile Web</strong>:
         Making a Web Site Accessible Both for People with Disabilities and for Mobile Devices</a></dt>
      <dd>Introduces how websites can more efficiently be designed to be accessible from a mobile device and also accessible for people with disabilities when developers understand the significant overlap between the two design goals and guidelines. Links to more detailed technical documents.</dd>
      <dt><strong><a href="http://www.w3.org/WAI/Resources/" shape="rect">WAI Resources</a></strong></dt>
      <dd>Lists the primary resources from W3C Web Accessibility Imitative (WAI), including several that provide additional information for organizations developing a business case.</dd>
   </dl>
   <p>Business cases are sometimes accompanied by an implementation plan
      describing the steps involved in making an organization's website
      accessible. A separate WAI resource suite, <a href="http://www.w3.org/WAI/impl/" shape="rect">Implementation Plan for Web
         Accessibility</a>, provides information on initial assessment, developing
      organizational policies, training, selecting authoring tools, and conformance
      evaluation.</p>
