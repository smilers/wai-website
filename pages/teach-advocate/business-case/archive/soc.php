   <h2><a name="intro" id="intro" shape="rect"></a>Introduction</h2>
   <p>This page is part of a resource suite that describes the social, <a href="tech.html" shape="rect"><strong>technical</strong></a>, <a href="fin.html" shape="rect"><strong>financial</strong></a>, and <a href="pol.html" shape="rect"><strong>legal and policy</strong></a> factors relevant to <a href="Overview.html" shape="rect">developing a customized business case for Web
      accessibility</a> for a specific organization.</p>
   <p>The importance of various social aspects of Web accessibility is
      different for specific organizations and situations. For example, one
      organization's goal might be to become a leader in corporate social
      responsibility, a different organization might be particularly interested in
      attracting capital from socially responsible investing sources, and another
      organization might be interested in demonstrating its focus on a specific
      social group, such as <a href="#of" shape="rect">older people</a>.</p>
   <p>This page provides guidance on addressing social factors in a business case for Web accessibility.</p>
   <h2><a name="custom" id="custom" shape="rect">Identifying Social Factors for a Specific
      Organization</a></h2>
   <p>The following questions can help identify how the social aspects of Web
      accessibility apply to the organization:</p>
   <ul>
      <li class="listspaced"><strong>Does the organization have a corporate
         social responsibility (CSR) statement or program?<br clear="none" />
         </strong>An organization's commitment to social responsibility could be
         called something other than
         "corporate social responsibility". See <a href="#responsible" shape="rect">Web
            Accessibility is an Aspect of Corporate Social Responsibility (CSR)</a>.</li>
      <li class="listspaced"><strong>Do  people within the  organization understand how Web
         accessibility fits into social responsibility?</strong><br clear="none" />
         If not, the business case can explain that <a href="#social" shape="rect">Web
            accessibility is a social issue</a> affecting equal opportunity.</li>
      <li class="listspaced"><strong>Are there other programs or initiatives
         within the organization that overlap with the social factors of Web
         accessibility?</strong> <br clear="none" />
         For example, if the organization has a strong emphasis on recruiting, the
         business case can include the benefits of attracting potential employees
         who want to work for an organization that is active in social
         responsibility.</li>
      <li class="listspaced"><strong>Does the organization focus on other groups
         that benefit from Web accessibility?<br clear="none" />
         </strong> For example, if the organization focuses on older people, the
         business case can include details on how Web accessibility benefits older
         people. See <a href="#older" shape="rect">Overlap with Older Users' Needs</a>.</li>
   </ul>
   <h2><a name="social" id="social" shape="rect">Web Accessibility is a Social Issue</a></h2>
   <p>Web accessibility focuses on people with all types of disabilities -  visual, auditory, physical, speech, cognitive, and neurological  disabilities - including older people with age-related impairments.</p>
   <h3>Web Accessibility is Essential for Equal Opportunity</h3>
   <p>Use of the Web is spreading rapidly into most areas of society and daily life. In many
      countries the Web is increasingly used for government information and
      services, education and training, commerce, news, workplace interaction,
      civic participation, health care, recreation,  entertainment, and more. In some cases, the Web is replacing traditional
      resources and service delivery.</p>
   <p>The Web is an important medium for
      receiving information as well as for providing information and interacting with
      society. Therefore, <strong>it is essential that the Web is accessible in order to provide equal access and equal opportunity to people with disabilities</strong>. This basic human right is recognized in the <a href="http://www.un.org/disabilities/default.asp?navid=12&amp;pid=150" shape="rect"><acronym title="United Nations">UN</acronym> Convention on the Rights of Persons with Disabilities</a>, which specifically mentions the Internet and other accessible information and communications technologies (ICT). An accessible Web can also help people with disabilities and older people more actively participate in society.</p>
   <p><strong>The Web is an opportunity for unprecedented access to information
      for people with disabilities.</strong> That is, the accessibility barriers to print, audio, and visual media can be much more easily overcome through web technologies. For example, when the primary way to
      get certain information was go to a library and read it on paper,  there were significant barriers for many people with disabilities, including getting to
      the library, physically getting the resource, and reading the resource.</p>
   <p>When
      that same information is also available on the Web in an accessible format,  it is
      significantly easier for many people  to access the information. Therefore, people with disabilities can have more effective and efficient
      access to information through accessible websites â€” in some cases, where
      there was essentially no access to it before.</p>
   <p><strong>The Web is an opportunity for unprecedented <em>interaction</em> for people
      with disabilities.</strong> For example, some disabilities limit the type of
      work a person can do and an accessible Web can increase their employment options. An accessible Web also expands opportunities for
      communication, social interaction, and community participation for people with disabilities and older people with age-related impairments.</p>
   <h3>Barriers to Web Use </h3>
   <p>Currently <strong>there are significant barriers on the Web for many people with disabilities</strong>. Because most web developers do not make their web pages and web tools accessible, many people with accessibility needs have unnecessary difficulties using the Web, and in some cases, cannot effectively use the Web at all. For example, when developers require mouse interaction to use a website, people who cannot use a mouse can have great difficulty; and when developers do not include alternative text for important images, people who are blind cannot get the information from images. Many of these barriers also impact older users with accessibility needs due to ageing.</p>
   <p>However, when websites are accessible, they enable people with disabilities to use the Web effectively. The document <a href="http://www.w3.org/WAI/intro/people-use-web" shape="rect">How People with Disabilities Use the Web</a> includes scenarios that describe people with different disabilities successfully using the Web. </p>
   <h3><a name="stats" id="stats" shape="rect"></a>Number of People Affected (statistics)</h3>
   <p><strong>Estimating how many people are affected by Web accessibility is
      difficult</strong> for several reasons. Countries define disability differently and use different  methods   to determine the number of people with disabilities. Some common conditions that do affect people's use of the Web (such as color blindness) may not be considered disabilities in many countries.  Not all disabilities  affect access to the Web (for example, difficulty walking does not affect access to the Web, though difficulty moving one's hands does).   Additionally, some people   do not want to disclose their disability, and some older people do not consider their impairments a disability.</p>
   <p>The United Nations <a href="http://unstats.un.org/unsd/demographic/sconcerns/disability/default.htm" shape="rect">Human Functioning and Disability</a> page includes links to  data for different countries. Market research such as <a href="http://www.microsoft.com/enable/research/phase1.aspx" shape="rect">The Market for Accessible Technology - The Wide Range of Abilities and Its Impact on Computer Use</a> and <a href="http://www.microsoft.com/enable/research/phase2.aspx" shape="rect">Accessible Technology in Computing - Examining Awareness, Use, and Future Potential</a> illustrates a different approach to estimating the percentage of computer users who might benefit from Web accessibility. The <a href="http://www.w3.org/WAI/bcase/resources#statecom" shape="rect">Statistics on People with Disabilities and Web Use</a> section of the Resources page links to additional statistics, including on ageing demographics and age-related impairments.</p>
   <h3>Overlap with Digital Divide Issues</h3>
   <p class="listintro">The term "digital divide" is often used to refer to economic and social barriers to computer use for people <em>without</em> disabilities. Many people with disabilities are affected by the same economic and social factors, including  very low rates of employment and consequently low income. Together with barriers in the physical environment and in computer technologies, these factors can result in:</p>
   <ul class="listwithp">
      <li>lack of accessible mainstream web technologies (such as browsers and authoring tools)</li>
      <li>lack of effective, up-to-date <a href="http://www.w3.org/WAI/eval/users.html#at" shape="rect">assistive technologie</a><a href="http://www.w3.org/WAI/eval/users.html#at" shape="rect">s</a></li>
      <li>lack of opportunities for training to become proficient with web technologies</li>
      <li>limited access to a social environment that encourages web use </li>
      <li>limited access to high-bandwidth connections, or even to regular Web access</li>
   </ul>
   <p>An organization that is committed to reducing the digital divide can  include in its business case a description of how Web accessibility can reduce the impact of economic and social barriers
      to web use for people with disabilities.</p>
   <h3><a name="mobileoverlap" id="mobileoverlap" shape="rect"></a>Overlap with Mobile Access</h3>
   <p>In some parts of the world, most people use the Web only through a mobile phone, because they do not have  access to a desktop or laptop. The  overlap between mobile design/development and accessbilty is introduced in <a href="http://www.w3.org/WAI/mobile/overlap.html" shape="rect">Web Content Accessibility and Mobile Web</a>.</p>
   <h3><a name="older" id="older" shape="rect">Overlap with Older Users' Needs</a></h3>
   <p class="listintro">As more people live  longer and older people use the Web more, making the Web work well for  older users is becoming an increasingly important social factor. Many  older people have age-related impairments that can affect how they use the  Web, including declining:</p>
   <ul class="listwithp">
      <li> vision - including    reduced contrast sensitivity, color perception, and near-focus,  making it difficult to read web pages</li>
      <li>physical  ability - including   reduced dexterity and fine motor control, making it difficult to use a mouse and click small targets</li>
      <li>hearing - including difficulty hearing   higher-pitched sounds and   separating sounds, making it difficult to hear podcasts and other audio, especially when  there is  background music</li>
      <li> cognitive ability - including reduced short-term memory, difficulty  concentrating, and being easily distracted, making it difficult to  follow navigation and complete online tasks</li>
   </ul>
   <p> These issues overlap with the accessibility needs of people with  disabilities. Thus, websites and tools that are accessible to people with  disabilities are more accessible to older users as well. Specific  examples are listed in the <a href="soc.html#of" shape="rect">Access for Older People</a> section below.</p>
   <p>For detailed research on ageing age-related impairments and Web accessibility, see <a href="http://www.w3.org/WAI/intro/wai-age-literature" shape="rect">Web Accessibility for Older Users: A Literature Review</a>.</p>
   <h2><a name="groups" id="groups" shape="rect">Web Accessibility Benefits People With and <em>Without</em> Disabilities</a></h2>
   <p class="listintro">While the main focus of Web accessibility is people with disabilities,
      accessibility also benefits people without disabilities, including: </p>
   <ul class="listwithp">
      <li><a href="#of" shape="rect">older people</a></li>
      <li><a href="#lolit" shape="rect">people with low literacy or not fluent in the language</a></li>
      <li><a href="#lowtech" shape="rect">people with low bandwidth connections  or using older technologies</a></li>
      <li><a href="#newuser" shape="rect">new and infrequent users</a></li>
      <li><a href="#mobileusers" shape="rect">mobile phone users</a></li>
   </ul>
   <p>The <a href="fin.html#increase-use" shape="rect">Increased Website Use</a> section of Financial Factors lists aspects of
      Web accessibility that increase usability, thus also benefiting people
      without disabilities. People with temporary disabilities, for example from
      an accident or illness, also benefit from Web accessibility.</p>
   <p>Below are examples of how Web accessibility benefits
      others. The <a href="/WAI/intro/wcag.php" shape="rect">Web
         Content Accessibility Guidelines (WCAG) Overview</a> has information about the WCAG references.</p>
   <h3><a name="of" id="of" shape="rect">Access for Older People</a></h3>
   <p>The accessibility provisions that make the Web accessible provide many benefits for people with age-related impairments, even though they may not be regarded as having a disability. For example:</p>
   <p class="listintro">Older people with deteriorating vision benefit from:</p>
   <ul class="listwithp">
      <li>sufficient contrast between foreground and background colors <br clear="none" />
            <span class="sc-grey">(WCAG 2.0 success criteria 1.4.3, 1.4.6; WCAG 1.0
               checkpoint 2.2)</span></li>
      <li>text that can be increased in size so that it can be read directly by people with mild visual impairment without requiring assistive technology such as a screen magnifier; along with easy to read fonts and increased line spacing<br clear="none" />
            <span class="sc-grey">(WCAG 2.0 success criteria 1.4.4, 1.4.8; WCAG 1.0 checkpoint 3.4)</span></li>
      <li>styled text instead of bitmap images of text to convey information enables
         better browser-based enlargement<br clear="none" />
               <span class="sc-grey">(WCAG 2.0 success criteria 1.4.5, 1.4.9; WCAG 1.0 checkpoint 3.1)</span></li>
      <li>text and other elements that do not blink, flash or move in a way that distract users or cause seizures <br clear="none" />
            <span class="sc-grey">(WCAG 2.0 success criteria 2.2.2, 2.3.1, 2.3.2; WCAG 1.0 checkpoints 7.2, 7.3)</span></li>
   </ul>
   <p class="listintro">Older people with reduced dexterity or fine motor control benefit from:</p>
   <ul class="listwithp">
      <li>an ability to increase the clickable area of targets<br clear="none" />
            <span class="sc-grey">(WCAG 2.0 success criteria 1.4.4, 1.4.5, 1.4.8; WCAG 1.0 checkpoint 3.4)</span></li>
      <li>being able to use the keyboard, rather than having
         to use the mouse, for all website interaction (device independence)<br clear="none" />
               <span class="sc-grey">(WCAG 2.0 success criteria 2.1.1, 2.1.2, 2.1.3, 2.4.7; WCAG 1.0 checkpoints 6.4,
                  9.1, 9.2, 9.3, 9.4, 9.5)</span></li>
   </ul>
   <p class="listintro">Older people with  hearing loss benefit from:</p>
   <ul class="listwithp">
      <li>transcripts and captions for audio  content <br clear="none" />
            <span class="sc-grey">(WCAG 2.0 success criteria 1.1.1, 1.2.2, 1.2.4, 1.2.8; WCAG 1.0 checkpoints 1.1, 1.4)</span></li>
      <li>contrast between the audio foreground  "information" and background "noise"<br clear="none" />
            <span class="sc-grey">(WCAG 2.0 success criteria 1.4.7)</span></li>
   </ul>
   <p>Older people with cognitive decline  benefit from many of the accessibility aspects list in the next section, Access for People with Low Literacy and People Not Fluent in the Language.</p>
   <p>Additional aspects of Web accessibility that benefit older users are included in the analysis in the <a href="http://www.w3.org/WAI/intro/wai-age-literature.php" shape="rect">Web Accessibility for Older Users: A Literature Review</a>.</p>
   <h3><a name="lolit" id="lolit" shape="rect">Access for People with Low Literacy and
      People Not Fluent in the Language</a></h3>
   <p class="listintro">Accessible websites benefit people with low literacy and
      people who are not fluent in the language of the website. Specifically, many
      of the aspects of Web accessibility for people with cognitive disabilities
      help people who do not know the language well, including:</p>
   <ul class="listwithp">
      <li>clear and simple language<br clear="none" />
            <span class="sc-grey">(WCAG 2.0 success criteria 3.1.5; WCAG 1.0 checkpoint 14.1)</span></li>
      <li>supplemental illustrations <br clear="none" />
            <span class="sc-grey">(WCAG 2.0 Guideline 3.1; WCAG 1.0 checkpoint 14.2)</span></li>
      <li>clear and consistent design, navigation, and links <br clear="none" />
            <span class="sc-grey">(WCAG 2.0 success criteria 1.3.1, 2.4.1, 2.4.2, 2.4.4, 2.4.5, 2.4.6, 2.4.9, 2.4.10, 3.2.3, 3.2.4, 3.3.2; WCAG 1.0 checkpoints
               13.1, 13.3, 13.4, 13.5, 13.6, 13.7, 13.8, 14.3)</span></li>
      <li>blocks of information divided into groups <br clear="none" />
            <span class="sc-grey">(WCAG 2.0 success criteria 1.3.1, 2.4.1, 2.4.10; WCAG 1.0 checkpoint
               12.3)</span></li>
      <li>text that does not blink, flash or move too much<br clear="none" />
            <span class="sc-grey">(WCAG 2.0 success criteria 2.2.2; WCAG 1.0 checkpoints 7.2, 7.3)</span></li>
      <li>provide users enough time to read and use content<br clear="none" />
            <span class="sc-grey">(WCAG 2.0 success criteria 2.2.1, 2.2.2, 2.2.3; WCAG 1.0 checkpoints 7.4, 7.5)</span></li>
   </ul>
   <p>In addition, accessible sites can be read by screen readers so people who have difficulty reading can benefit from listening to sites.</p>
   <h3> <a name="lowtech" id="lowtech" shape="rect">Access for People with Low Bandwidth Connections to the Internet or Using Older Technologies</a> </h3>
   <p class="listintro">Some aspects of Web accessibility benefit people with low bandwidth
      connections. Low bandwidth can be due to:</p>
   <ul class="listwithp">
      <li>location- for example, rural areas where high speed connections are not available or mobile phone reception is limited</li>
      <li>bandwidth congestion</li>
      <li>connection technology- for example, mobile phone or personal data
         assistant (PDA)</li>
      <li>financial situation- that is, cannot afford high-speed connection</li>
   </ul>
   <p>Some older technologies load pages very slowly and do not support features
      used on newer sites.</p>
   <p class="listintro">People with low bandwidth connections to the Internet and people with older technologies benefit
      from:</p>
   <ul class="listwithp">
      <li>text alternatives for images, multimedia and other non-text objects - for people whose older
         technology or mobile technology cannot access multimedia formats, people whose
         connections are too slow to download multimedia files, or people who turn off images and multimedia to limit connection charges<br clear="none" />
               <span class="sc-grey">(WCAG 2.0 success criteria 1.1.1, 1.2.8; WCAG 1.0 checkpoint
                  1.1)</span></li>
      <li>redundant coding for information conveyed with color and sufficient
         contrast between foreground and background colors - for people who have older monitors or are viewing information outdoors where the sunlight makes it difficult to see the screen<br clear="none" />
               <span class="sc-grey">(WCAG 2.0 success criteria 1.4.1, 1.4.3, 1.4.6; WCAG 1.0 checkpoints 2.1, 2.2)</span></li>
      <li>text size defined as relative units - for older browsers that do not
         override absolute text sizes <br clear="none" />
               <span class="sc-grey">(WCAG 2.0 success criteria 1.4.4, 1.4.8; WCAG 1.0 checkpoint 3.4)</span></li>
      <li>styled text instead of bitmap images of text to convey
         information, which can increase download speed <br clear="none" />
               <span class="sc-grey">(WCAG 2.0 success criteria 1.4.5, 1.4.9; WCAG 1.0 checkpoint
                  3.1)</span></li>
      <li>style sheets used effectively to separate content from presentation,
         which can decrease file size and file download requirements thus
         increasing download speed <br clear="none" />
               <span class="sc-grey">(WCAG 2.0 success criteria 1.3.1; WCAG 1.0 checkpoint 3.3)</span></li>
      <li>sites that are organized so they can be read and understood without style sheets,
         because some older technologies cannot handle style sheets (accessible
         pages can use style sheets and still be usable when style sheets are not
         supported)<br clear="none" />
               <span class="sc-grey">(WCAG 2.0 success criteria 1.3.1, 1.3.2; WCAG 1.0 checkpoint 6.1)</span></li>
      <li>sites that use valid W3C technologies and are more likely to work
         on older technologies <br clear="none" />
               <span class="sc-grey">(WCAG 2.0 success criteria 4.1.1; WCAG 1.0 checkpoints 11.1, 3.2)</span></li>
      <li>clear and consistent design, navigation, and links, which helps users
         open the pages they want and helps save wasted page loading time from
         users going down the wrong path <br clear="none" />
               <span class="sc-grey">(WCAG 2.0 success criteria 2.4.1, 2.4.4, 2.4.5, 2.4.6, 2.4.9, 3.2.3, 3.2.4; WCAG 1.0 checkpoints 13.1, 13.3, 13.4,
                  13.5, 13.6, 13.7, 13.8, 14.3)</span></li>
   </ul>
   <h3><a name="newuser" id="newuser" shape="rect">Access for New and Infrequent Web Users</a></h3>
   <p class="listintro">Some people have little opportunity to use the Web because of the
      socioeconomic issues mentioned previously. Many older people are new users because the Web didn't exist when they were younger. New and infrequent web users
      benefit from aspects of accessibility such as:</p>
   <ul class="listwithp">
      <li>clear and consistent design, navigation, and links <br clear="none" />
            <span class="sc-grey">(WCAG 2.0 success criteria 2.4.1, 2.4.4, 2.4.5, 2.4.6, 2.4.9, 3.2.3, 3.2.4; WCAG 1.0 checkpoints
               13.1, 13.3, 13.4, 13.5, 13.6, 13.7, 13.8, 14.3)</span></li>
      <li>providing redundant text links for image maps <br clear="none" />
            <span class="sc-grey">(WCAG 2.0 success criteria 2.4.4; WCAG 1.0 checkpoint
               1.2)</span></li>
      <li>informing users before new browser windows are opened<br clear="none" />
            <span class="sc-grey">(WCAG 2.0 success criteria 3.2.1, 3.2.2, 3.2.5; WCAG 1.0 checkpoint
               10.1)</span></li>
   </ul>
   <h3><a name="mobileusers" id="mobileusers" shape="rect"></a>Access for Mobile Device Users</h3>
   <p>For examples of how accessibility benefits people using mobile devices, along with links to WCAG success criteria, see <a href="http://www.w3.org/WAI/mobile/experiences" shape="rect">Shared Web Experiences</a>.</p>
   <h2><a name="responsible" id="responsible" shape="rect">Web Accessibility is an Aspect of
      Corporate Social Responsibility (CSR)</a></h2>
   <p>Web accessibility provides improved access, interaction, and social inclusion for the people  described above, which is a primary aspect of corporate social responsibility (CSR).</p>
   <p>Corporate social responsibility, also called corporate citizenship, corporate responsibility, or responsible business,
      generally means conducting business ethically and operating an organization in
      such a way that  treats internal and external stakeholders ethically,
      increases human development, and is good for society and the
      environment. Web accessibility can impact an organization's employees,
      stockholders and board members, suppliers and vendors, partners and
      collaborators, customers, and others. Thus Web accessibility is
      an integral part of CSR in demonstrating an organization's commitment to
      providing equal opportunities.</p>
   <p>Just as an accessible website can demonstrate CSR, an inaccessible website can undermine an organization's other CSR efforts.</p>
   <p>The financial benefits of CSR are addressed in the <a href="fin.html#image" shape="rect">Increases positive image</a> section of the Financial
      Factors page.</p>
   <h2><a name="role" id="role" shape="rect">Role of Organizations' Websites</a></h2>
   <p>When an organization's website <strong>is not accessible, it
      further excludes</strong> people with disabilities from society. When an
      organization's website <strong>is accessible, it empowers </strong>people with disabilities to participate in society. Providing an accessible website is
      one way an organization can demonstrate that it strives to meet the access
      needs of a diverse society.</p>
